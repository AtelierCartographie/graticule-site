<section id="faq">
    <h3>FAQ</h3>
    
    <details>
        <summary>À quoi sert Graticule ?</summary>
        Obtenir un fond de carte pour un croquis géographique, découvrir et manipuler les projections cartographiques, mobiliser des notions en design graphique. Les usages et les publics peuvent être très variés. Notre volonté derrière Graticule est de mettre à disposition un outil gratuit et simple d’utilisation permettant d’initier tout type de public à la cartographie et ses concepts.
    </details>

    <details>
        <summary>Qui est derrière l’outil Graticule ?</summary>
        L’<a rel="external" href="https://www.sciencespo.fr/cartographie/" target="_blank">Atelier de cartographie de Sciences Po</a> a conçu et développé Graticule. Créé en 1995, l’Atelier contribue à la formation, élabore des contenus pédagogiques et scientifiques, participe à des projets de recherche et à des publications ou encore expérimente des outils de visualisation. Ainsi, il participe aux missions de Sciences Po de production et de diffusion de savoirs qui éclairent les enjeux contemporains et alimentent le débat public.
        <br>
        Avec Graticule, l’Atelier de cartographie espère proposer un outil pédagogique, principalement à destination des élèves et étudiants ainsi qu’à un public d’enseignants.
    </details>

    <details>
        <summary>Est-ce possible d’importer et de visualiser des données ?</summary>
        Non, Graticule est un outil permettant de réaliser des fonds de carte uniquement. Quelques informations élémentaires sont disponibles au sein de l’outil pour être ajoutées au fond de carte. Ce n’est donc pas un outil de cartographie thématique. Pour un tel besoin, nous vous invitons à utiliser <a rel="external" href="https://www.sciencespo.fr/cartographie/khartis/" target="_blank">Khartis</a> qui permet de réaliser simplement des cartes thématiques.
    </details>

    <details>
        <summary>Puis-je utiliser un fond de carte issu de Graticule dans un SIG ou un logiciel de cartographie ?</summary>
        Non, les exports tirés de Graticule sont destinés à l’impression ou à l’édition sur des logiciels d’édition d’image ou de dessin vectoriel. Les données utilisées dans l’outil sont néanmoins accessibles librement grâce aux fournisseurs de données indiqués dans les sources. Il est donc possible de télécharger ces données et les exploiter sur le logiciel de votre choix.
    </details>

    <details>
        <summary>Ai-je le droit d’utiliser un fond de carte issu de Graticule pour un usage commercial ?</summary>
        Oui, Graticule est un outil open source, utilisant des données libres de droits permettant tout type d’usage, même commercial. En revanche, les sources indiquées au bas de la carte sont à conserver sur votre carte finale. La mention “réalisé avec Graticule” est elle facultative, mais la conserver est un soutien de votre part qui reste bienvenu !
    </details>

    <details>
        <summary>Quelles sont les sources utilisées ?</summary>
        Graticule utilise des sources disponibles en open access et libres de droits. Les fournisseurs de ces données sont indiqués en bas du fond de carte selon les informations affichées.
    </details>

    <details>
        <summary>Qu’est ce qu’un graticule ?</summary>
        C’est une représentation graphique sous forme de grille, composée de méridiens et de parallèles, permettant de visualiser un système de coordonnées et de mieux apprécier une projection cartographique. C’est aussi une très bonne idée de nom d’application !
    </details>
